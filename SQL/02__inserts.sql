SELECT 
    N.IDNOTA,
    N.TOTAL AS TOTAL_NOTA,
    SUM(I.TOTAL) AS TOTAL_ITENS
FROM NOTA_FISCAL N
INNER JOIN ITEM_NOTA I
    ON N.IDNOTA = I.ID_NOTA_FISCAL
GROUP BY N.IDNOTA, N.TOTAL
HAVING N.TOTAL <> SUM(I.TOTAL);
GO

