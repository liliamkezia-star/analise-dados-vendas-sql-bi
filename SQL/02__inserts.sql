DECLARE
    @ID_CLIENTE INT,
    @ID_VENDEDOR INT,
    @ID_FORMA INT,
    @ANO INT,
    @MES INT,
    @DIA INT,
    @DATA DATE;

BEGIN
    SET @ID_CLIENTE = (SELECT TOP 1 IDCLIENTE FROM CLIENTE ORDER BY NEWID());
    SET @ID_VENDEDOR = (SELECT TOP 1 IDVENDEDOR FROM VENDEDOR ORDER BY NEWID());
    SET @ID_FORMA = (SELECT TOP 1 IDFORMA FROM FORMA_PAGAMENTO ORDER BY NEWID());

    SET @ANO = 2022 + (ABS(CHECKSUM(NEWID())) % 4); -- 2022 a 2025
    SET @MES = (ABS(CHECKSUM(NEWID())) % 12) + 1;
    SET @DIA = (ABS(CHECKSUM(NEWID())) % 28) + 1;

    SET @DATA = DATEFROMPARTS(@ANO, @MES, @DIA);

    INSERT INTO NOTA_FISCAL (DATA, TOTAL, ID_FORMA, ID_CLIENTE, ID_VENDEDOR)
    VALUES (@DATA, 0, @ID_FORMA, @ID_CLIENTE, @ID_VENDEDOR);
END
GO 3000

