    @ID_NOTA_FISCAL INT,
    @QUANTIDADE INT,
    @VALOR_UNITARIO NUMERIC(10,2),
    @TOTAL NUMERIC(10,2);

BEGIN
    SET @ID_PRODUTO = 
        (SELECT TOP 1 IDPRODUTO FROM PRODUTO ORDER BY NEWID());

    SET @ID_NOTA_FISCAL = 
        (SELECT TOP 1 IDNOTA FROM NOTA_FISCAL ORDER BY NEWID());

    SET @QUANTIDADE = (ABS(CHECKSUM(NEWID())) % 4) + 1;

    SET @VALOR_UNITARIO = 
        (SELECT VALOR
         FROM PRODUTO
         WHERE IDPRODUTO = @ID_PRODUTO);

    SET @TOTAL = @QUANTIDADE * @VALOR_UNITARIO;

    INSERT INTO ITEM_NOTA (
        ID_PRODUTO,
        ID_NOTA_FISCAL,
        QUANTIDADE,
        VALOR_UNITARIO,
        TOTAL
    )
    VALUES (
        @ID_PRODUTO,
        @ID_NOTA_FISCAL,
        @QUANTIDADE,
        @VALOR_UNITARIO,
        @TOTAL
    );
END
