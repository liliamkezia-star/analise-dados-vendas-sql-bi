GO
  
-- 7) CIDADES COM MAIOR CRESCIMENTO 

WITH FATURAMENTO_MENSAL AS (
    SELECT 
        E.CIDADE,
        DATEFROMPARTS(YEAR(N.DATA), MONTH(N.DATA), 1) AS MES,
        SUM(N.TOTAL) AS FATURAMENTO
    FROM NOTA_FISCAL N
    INNER JOIN CLIENTE C ON N.ID_CLIENTE = C.IDCLIENTE
    INNER JOIN ENDERECO E ON C.IDCLIENTE = E.ID_CLIENTE
    GROUP BY 
        E.CIDADE,
        YEAR(N.DATA),
        MONTH(N.DATA)
)
SELECT 
    CIDADE,
    MES,
    FATURAMENTO,
    LAG(FATURAMENTO) OVER (PARTITION BY CIDADE ORDER BY MES) AS FATURAMENTO_ANTERIOR
FROM FATURAMENTO_MENSAL;
GO
  
-- 8) PARTICIPAÇÃO DE CADA CIDADE

SELECT 
    E.CIDADE,
    SUM(N.TOTAL) AS FATURAMENTO,
    SUM(N.TOTAL) * 100.0 / SUM(SUM(N.TOTAL)) OVER () AS PARTICIPACAO
FROM NOTA_FISCAL N
INNER JOIN CLIENTE C ON N.ID_CLIENTE = C.IDCLIENTE
INNER JOIN ENDERECO E ON C.IDCLIENTE = E.ID_CLIENTE
GROUP BY E.CIDADE
ORDER BY FATURAMENTO DESC;
GO
  
-- 9) CIDADES SEM VENDA

SELECT 
    DISTINCT E.CIDADE
FROM ENDERECO E
LEFT JOIN CLIENTE C ON E.ID_CLIENTE = C.IDCLIENTE
LEFT JOIN NOTA_FISCAL N ON C.IDCLIENTE = N.ID_CLIENTE
WHERE N.ID_CLIENTE IS NULL;
GO
  
-- 10) FATURAMENTO POR CIDADE POR ANO

SELECT 
    YEAR(N.DATA) AS ANO,
    E.CIDADE,
    SUM(N.TOTAL) AS FATURAMENTO
FROM NOTA_FISCAL N
INNER JOIN CLIENTE C ON N.ID_CLIENTE = C.IDCLIENTE
INNER JOIN ENDERECO E ON C.IDCLIENTE = E.ID_CLIENTE
GROUP BY YEAR(N.DATA), E.CIDADE
ORDER BY ANO, FATURAMENTO DESC;
GO
  
