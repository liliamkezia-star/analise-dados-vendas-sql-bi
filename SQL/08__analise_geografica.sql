-- 1) FATURAMENTO POR CIDADE

SELECT 
    E.CIDADE,
    E.ESTADO,
    SUM(N.TOTAL) AS FATURAMENTO
FROM NOTA_FISCAL N
INNER JOIN CLIENTE C
    ON N.ID_CLIENTE = C.IDCLIENTE
INNER JOIN ENDERECO E
    ON C.IDCLIENTE = E.ID_CLIENTE
GROUP BY 
    E.CIDADE,
    E.ESTADO
ORDER BY FATURAMENTO DESC;
GO
  
-- 2) TOP 10 CIDADES

SELECT TOP 10
    E.CIDADE,
    E.ESTADO,
    SUM(N.TOTAL) AS FATURAMENTO
FROM NOTA_FISCAL N
INNER JOIN CLIENTE C
    ON N.ID_CLIENTE = C.IDCLIENTE
INNER JOIN ENDERECO E
    ON C.IDCLIENTE = E.ID_CLIENTE
GROUP BY 
    E.CIDADE,
    E.ESTADO
ORDER BY FATURAMENTO DESC;
GO
  
